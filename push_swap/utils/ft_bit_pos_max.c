#include "push_swap.h"

int	ft_bit_pos_max(int num_max)
{
	int	i;
	int	pos;

	i = -1;
	pos = 0;
	while (++i < 32)
	{
		if (((num_max >> i) & 1) == 1)
			pos = i;
	}
	return (pos);
}
