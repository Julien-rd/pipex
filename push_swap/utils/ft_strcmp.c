#include "push_swap.h"

int	ft_strcmp(char *str1, char *str2)
{
	if ((str1[0] == 'p' && str1[1] == 'a' && str2[0] == 'p' && str2[1] == 'b')
		|| (str1[0] == 'p' && str1[1] == 'b' && str2[0] == 'p'
			&& str2[1] == 'a'))
		return (1);
	if ((str1[0] == 'r' && str1[1] == 'a' && str2[0] == 'r' && str2[1] == 'r'
			&& str2[2] == 'a') || (str1[0] == 'r' && str1[1] == 'r'
			&& str1[2] == 'a' && str2[0] == 'r' && str2[1] == 'a'))
		return (1);
	return (0);
}
